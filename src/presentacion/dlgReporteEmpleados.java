    public dlgReporteEmpleados() {
        super(FrmPrincipal.getInstancia(),false);
        cnn = Conexion.getInstancia().miConexion();
        initComponents();
        setLocationRelativeTo(null);modelo.setColumnIdentifiers(nombreColumnas);
        try {
            ArrayList<Empleado> lista = EmpleadoDAO.getInstancia().mostrarEmpleados();
            Object [][]datos=new Object[lista.size()][9];
            for (int i = 0; i < lista.size(); i++) {
                Empleado x = lista.get(i);
                datos[i][0]=x.getEmplcodigo();
                datos[i][1]=x.getEmplpaterno();
                datos[i][2]=x.getEmplmaterno();
                datos[i][3]=x.getEmplnombre();
                datos[i][4]=x.getEmplciudad();
                datos[i][5]=x.getEmpldireccion();
                datos[i][6]=x.getEmplusuario();
                datos[i][7]=x.getEmplclave();
            }
            modelo.setDataVector(datos, nombreColumnas);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        };
    }

