    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String codigo;
        int p;
        limpiarTextos();
        codigo = JOptionPane.showInputDialog("El codigo del cliente a eliminar : ");
        Cliente x;
        try {
            x = ClienteDAO.getInstancia().buscarCliente(codigo);
            if (x != null) {
                txtCodigo.setText(x.getCliecodigo());
                txtApellidosPaternos.setText(x.getCliepaterno());
                txtApellidosMaternos.setText(x.getCliematerno());
                txtNombres.setText(x.getClienombre());
                txtDNI.setText(x.getCliedni());
                txtCiudad.setText(x.getClieciudad());
                txtDireccion.setText(x.getCliedireccion());
                txtTelefono.setText(x.getClietelefono());
                txtEmail.setText(x.getClieemail());
                int resp = JOptionPane.showConfirmDialog(this, "Deseas Eliminarlo ", "Eliminar Dato", JOptionPane.YES_NO_OPTION);
                if (resp == JOptionPane.YES_OPTION) {
                    ClienteDAO.getInstancia().eliminar(codigo);
                    JOptionPane.showMessageDialog(this, "Registro eliminado");
                }
                limpiarTextos();
            } else {
                JOptionPane.showMessageDialog(null, "El codigo del cliente no existe");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

