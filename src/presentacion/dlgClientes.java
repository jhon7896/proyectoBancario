    void generarCodigoCliente() throws SQLException {
        String serie = ClienteDAO.getInstancia().codigoCliente();
        if (serie == null) {
            txtCodigo.setText("00001");
        } else {
            int increment = Integer.parseInt(serie);
            increment = increment + 1;
            if(txtCodigo.getText().compareTo("00009")==0){
                txtCodigo.setText("0000" + increment);
            }else{
                txtCodigo.setText("000" + increment);
            }
        }
    }
    
